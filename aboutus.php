        <p>Friendly Ballot was created in 2020 as a part of two students' computer engineering project. The goals
            that were aimed to be achieved through Friendly Ballot:-</p>
            <ul>
                <li>Provide the best kind of experience for ballor administrators, candidates and voters. Getting voters
                    to cast vote is challenging and that is why Friendly Ballot exhibits a friendly user interface.
