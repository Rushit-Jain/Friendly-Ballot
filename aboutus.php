                        <li>Voter privacy is maintained and not even the ballor administrator can see to whom the vote is cast,
                            hence making Friendly Ballot a secret ballot too.</li>
                            <li>Friendly Ballot is cost effective, in fact, it is completely free! Anyone, in any part of the world,
